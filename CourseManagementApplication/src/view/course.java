    public void addLecturersToCombo(){
        lectComboBox.removeAllItems();
        LecturerDAO lect = new LecturerDAO();
        ResultSet lecturerData = lect.getAllLecturers();
        try {
            while(lecturerData.next()) {
                lectComboBox.addItem(lecturerData.getString(1) +" - "+ lecturerData.getString(2));
            }
        } catch(Exception ex) {
            ex.printStackTrace();
        }
    }
    public void addStudentList() {
        studentModel = new DefaultListModel();
        StudentDAO stud = new StudentDAO();
        ResultSet studentData = stud.getAllStudents();
        try {
            while(studentData.next()) {
                studentModel.addElement(studentData.getString(1) +" - "+ studentData.getString(2));
            }
        } catch(Exception ex) {
            ex.printStackTrace();
        }
        stList.setModel(studentModel);
    }
