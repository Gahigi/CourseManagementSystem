    private void stSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_stSaveActionPerformed
        if(stIdTxt.getText().isEmpty() || stNamesTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter Data");
        } else {
            Student stud = new Student();
            stud.setStudent_id(stIdTxt.getText());
            stud.setStudent_names(stNamesTxt.getText());
            StudentDAO dao = new StudentDAO();
            String feedback = dao.saveStudent(stud);
            JOptionPane.showMessageDialog(this, feedback);
            addStudentList();
        }
    }//GEN-LAST:event_stSaveActionPerformed

    private void lectSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lectSaveActionPerformed
        if(lectIdTxt.getText().isEmpty() || lectNameTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter Data");
        }else {
            Lecturer lect = new Lecturer();
            lect.setLecturer_id(lectIdTxt.getText());
            lect.setLecturer_names(lectNameTxt.getText());
            LecturerDAO dao =new LecturerDAO();
            String feedback = dao.saveLecture(lect);
            JOptionPane.showMessageDialog(this, feedback);
            addLecturersToCombo();
        }
    }//GEN-LAST:event_lectSaveActionPerformed

    private void cSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cSaveActionPerformed
       if(cIdTxt.getText().isEmpty() || cNameTxt.getText().isEmpty()) {
           JOptionPane.showMessageDialog(this, "Enter Data");
       } else {
           Course courseInfo = new Course();
           courseInfo.setCourse_id(cIdTxt.getText());
           courseInfo.setCourse_name(cNameTxt.getText());
           courseInfo.setLecturer_id_fk(lectComboBox.getSelectedItem().toString().substring(0, 5));
           CourseDAO courseDao = new CourseDAO();
           String feedback = courseDao.saveCourse(courseInfo);
           List studentInfo = stList.getSelectedValuesList();
           Iterator iterator = studentInfo.iterator();
           while(iterator.hasNext()) {
               StudentDAO dao = new StudentDAO();
               String Id = iterator.next().toString().substring(0, 5);
               dao.updateStudent(Id, cIdTxt.getText());
           }
           JOptionPane.showMessageDialog(this, feedback);
       }
    }//GEN-LAST:event_cSaveActionPerformed

